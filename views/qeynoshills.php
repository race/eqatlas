      <h3><font size=+2>D</font>escription</h3>
      <p>Qeynos Hills is an area one step above the newbie area of North Qeynos,
        and provides good hunting for the levels above, although it can be heavily
        trafficked at times with monks looking to get their putrid bones and others
        looking for pelts. It is a small and easy zone to get around once you
        get your bearings, and is the hub of activity on the western coast of
        Antonica.
      <h3><font size=+2>D</font>angers </h3>
      <p>The danger with this zone, as with other zones, is that there are aggressive
        monsters wandering around that you don't want to get into a tangle with
        at too low a level. For instance, skeletons and grizzly bears, as well
        as rabid wolves are very deadly to many, and probably result in most of
        the deaths of people in this area.
      <p>Another danger are the two ranger NPC's wandering the zone. I haven't
        found a use for them, but can't count the number of times I've cursed
        them for killing me when I was attacked by a rabid wolf. You see, they
        take it upon themselves to keep "poachers" out of the area, which includes
        any character fighting either a wolf or bear, regardless of who started
        the fight. Keep your eyes peeled for these if you run into problems with
        either of those two monsters, and consider carefully the benefits of hunting
        wolves in this area, because they are harder to avoid than bears and will
        attack you constantly, and the two rangers don't care who picked on who,
        they'll kill you.
      <p>Gnasher also wanders through the zone randomly, usually around the entrance
        to Blackburrow. He has never attacked me personally, and he doesn't move
        too quickly, but if you hear the cry "Gnasher by BB" be sure to /con the
        gnoll you are attacking to make sure it isn't him. The people hiding at
        the zone edge to BB aren't going to be grateful if you run Gnasher to
        them and zone to escape him.
      <p>There is also two necromancers who wander the area with their pets, Pyzjin,
        a female gnome, and Tovax, a male human. These two are aggressive and
        tough, and like all NPC's fight at stronger then they /con to you.
      <p>The three camps of loathsome individuals are also dangerous places to
        wander past. These individuals are very aggressive with large aggro ranges,
        and come in groups. They can also be quite high level, and are known for
        becoming more powerful as they are killed off. They have recently left
        the area, but may return at any time.
      <p>Last but not least, putrid skeletons and dread corpses sometimes make
        appearances in the form of trains heading to Surefall Glade. I myself
        had quite an impressive one with at least four putrids and two dreads
        when I was helping my monk friend get the bones (which he never did get).
        Listen to /shout's and keep your eyes peeled.
      <h3><font size=+2>B</font>enefits</h3>
      <p>There is a wide range of monsters of low levels to fight in this area,
        and it offers a good area to fight some 2nd level monsters that is away
        from the big city hustle and bustle of North Qeynos. Included in the mix
        are fire beetles, whose eyes are a boon to all who can't see at night,
        and will sell for a pretty penny on faraway places like Faydwer (Rangers
        and Druids need the eyes as a spell component for one of their earliest
        spells).
      <p>This is a good area to get both small and large pelts for making patchwork
        leather armor (i.e. wolf and bear pelts). The only problem is that there
        aren't many people around this area who use small patchwork armor, so
        killing wolves in order to make it means you'll be selling it back to
        merchants for not much profit and manage to irritate the local rangers
        at the same time.
      <p>The zone is also relatively small, making it good for fleeing to zone
        edges to escape pursuing monsters.
      <h3><font size=+2>T</font>ravelling <font size=+2>T</font>o and <font size=+2>F</font>rom
        <font size=+2>Q</font>eynos <font size=+2>H</font>ills</h3>
      <p>From the North, Qeynos Hill is reached through Blackburrow, home of the
        Blackburrow Gnolls. This can be a dangerous place, and probably shouldn't
        be travelled by those less than about 4th or 5th level, you can still
        fly through as long as you keep your eyes peeled for trouble. On the other
        side is Everfrost and the barbarian home of Halas. Also to the North is
        Surefall Glade, home of the rangers and druids of the area. The area in
        front of Surefall Glade is a good newbie area, and generally less crowded
        than the one next to Qeynos.
      <p>From the South, Qeynos Hills is entered by coming out of North Qeynos,
        a city of humans and half-elves.
      <p>From the East, access is through the Western Plains of Karana, a higher
        level zone for people greater than 4th level. The lands beyond are even
        more dangerous, so don't think you can sneak through unless you're like
        me, a Bard on the wings of my drum.</p>
