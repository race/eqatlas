      <h3><font size=+2>D</font>escription</h3>
      <p>Lesser Faydark is a dark forest which, unlike its neighbor Greater Faydark,
        has been overrun by the evil orcs and bandits, as well as the much more
        powerful undead sprites and other corrupted creatures.. There are also
        small camps of Pixies, Faeries, and Brownies, as well as the mighty Fae
        Drakes, all of whom make this zone their home.
      <h3><font size=+2>D</font>angers </h3>
      <p>This is a mid-level zone, where you should really be in the mid-teens
        or higher to plan on surviving for very long. Although there are areas
        in the zone where you can hunt at much lower levels, the danger is that
        there are many wandering dangerous creatures in the zone now.
      <p>Most of the other monsters in this zone reside in camps. These monsters
        tend to not wander very much outside of their little camps. The only exception
        to this is the Brownie Scouts that wander the middle of the zone. They
        are very aggressive and almost as powerful as the Brownies in the town.
        Also, most of the other monsters that wander can begin to dislike you
        as you kill more of their kin. These include the Fae Drakes and the Pixies
        and Faeries, who are all frequently seen travelling through the zone,
        but are generally neutral to their neighbors. Equestrielle the Corrupted
        is occasionally seen in the zone, wandering around to the entire zone.
        She is also highly aggressive to all, and the bards will not assist you
        in fighting her.
      <p>One of the largest dangers in the zone, however, are the Brownies that
        live in the town to the north. These creatures of the forest are very
        aggressive, and are KOS to just about everyone (the only people I've seen
        spared are druids in Wolf Form). They are all at least 45th level and
        are Druids, so are capable of taking down even a very well trained group
        quickly. Accidentally walk near their town and you better know an invisibility
        spell for recovering your corpse... Unless, of course, you can take on
        wolf form, in which case they'll gladly sell to you...
      <p>There is another danger that recently surfaced, that of the Tier'Dal
        that currently inhabit the abandoned camp of Wu along the southern border
        of the camp. These Tier'Dal are aggressive and powerful, and the trains
        caused by someone inadvertantly stumbling across them can wreak havoc
        across the forest.
      <h3><font size=+2>B</font>enefits</h3>
      <p>If you are able to get over living in constant darkness and are powerful
        enough to fight off the undead sprites, this area provides good hunting
        for mid-level characters. The camps are usually not camped (well, the
        bandit camps are), and you can travel around making money off of the orcs
        that fill the southern half of the region. If you are brave, you can start
        making enemies of the other races in the area and make money off of them
        as well. They are all well equipped with loot and provide good money to
        an adventurer.
      <p>There are other, more subtle, benefits to this zone. People who want
        to rip our their hair in Greater Faydark with the constant chatter will
        find it remarkable absent in this zone. There is also a small enclave
        of very high level wood elves living in a small hut in the middle of the
        zone, who you can sell to for good prices as well as buy food from.
      <h3><font size=+2>T</font>ravelling <font size=+2>T</font>o and <font size=+2>F</font>rom
