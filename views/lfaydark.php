        that on the eastern wall lies the pass that leads into the Steamfont Mountains,
        home of the gnomes.
      <p>In the southwestern corner lies the narrow pass that separates the rest
        of the world from Castle Mistmoore, rumored to be home to an ancient dark
        elf vampire. Many who pass through here never return...
