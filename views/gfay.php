      <h3><font size=+2>D</font>escription</h3>
      <p>Greater Faydark is one of the largest and most confusing regions in all
        of the world of Norrath. It is quite large, and also has the added bonus
        of being dark at all times with few landmarks to tell where you are at
        any given time. It is the home of the wood elves, and is also the newbie
        area for the wood elves, high elves, and half elves that start out in
        Kelethin and Felwithe. It is also very near the homes of both the dwarves
        and the gnomes, so many members of those races are seen here as well.
      <p>Although Greater Faydark is a large zone, there are not too many areas
        of interest. Most of what can happen here is that one can get lost, so
        it's best to simply stick to the paths and not wander too far unless you
        know where you are going or have a good direction sense. The only area
        of note not on a path is Crushbone, and it is so close to Kelethin that
        you don't need to wander.
      <h3><font size=+2>D</font>angers </h3>
      <p>Greater Faydark is a relatively safe place to travel and fight in, barring
        the area near Crushbone which I will discuss further down. The rest of
        the zone is relatively safe, and the only inherently aggressive creatures
        here are the orcs. They tend to wander all over the zone, and include
        orc pawns which are second level near the cities and orc centurions further
        out (which can be as high as 9th). These are the biggest danger travelling
        away from the path and also occasionally on the path. Of the other monsters,
        none are notable except that they will all group to attack you if you
        are fighting others of their kind. Also, you can make some serious enemies
        by attacking wolves and pixies, so I wouldn't unless you know what you
        are doing. Notably, however, I have to hunt pixies as part of a ranger
        quest and have killed many of them and they still leave me alone.
      <p>Nearer Crushbone, however, things take on a different feel. There are
        no reliable guards nearby that you can run to, and the environment in
        terms of the orcs that roam here and the other players changes considerably.
        The orcs that appear here are only rarely ones that stay in one spot,
        and you have to be prepared for others wandering by to join in any battle
        you find yourself in. Also, the orcs here are tougher, and there was still
        pretty good hunting for me at 7th level when I'm spending most of my time
        in Crushbone. A lot of adventurers at level 3 or 4 wander over here, and
        it's no surprise that the ground is littered with their corpses.
      <p>The other problem with Greater Faydark, more of an annoyance really,
        is that it is vastly overcrowded. A few nights ago I logged on and there
        were 145 people in Greater Faydark. If I had logged into Innothule there
        would have been maybe 45 people. This means that it sometimes is very
        difficult to find creatures to hunt, and the only place to meet a good
        number of creatures to kill is in front of Crushbone, which I already
        discussed above. On this island, as a newbie, I would travel to the area
        in front of Kaladim and hunt there instead, or the little newbie area
        tucked away in the back of Steamfont Mountains, where there are less people
        and less danger.
      <p>The other thing to be aware of is that the guards in Greater Faydark
        are some of the densest guards around. They stand right next to you and
        watch you get pounded on before they consider responding, so I would run
        to them sooner than usual because you're going to need some life to last
        while they figure out that that centurion is indeed not a wood elf.
      <h3><font size=+2>B</font>enefits</h3>
      <p>Starting in Greater Faydark has got its problems, but despite that has
        many benefits as well. First of all, selling and buying items in Kelethin
        is very easy as it is the only starting city where you don't have to zone
        at all to reach it. Also, for other merchants the surrounding area is
        wonderful as Kelethin has most of the goods necessary to make items (with
        the occasional short jaunt to Steamfont for smithing supplies). This also
        works on the other end, because with 145 people logged on, there is a
        huge market for just about any good you can sell.
      <p>This area, like the other areas on the island, is excellent because you
        have ready access to all of the other zones of the island. This may not
        sound like much, but a lot of good work went into designing this island
        to be self-sufficient, and if you know the island well, there are places
        for you to go to hunt that are perfectly suited to your level, and are
        at most 2 short zones away.
      <p>An example might make this clear. My wood elf ranger started in Kelethin.
        He made his way to 4th level killing orc pawns and other creatures around
        the base of Kelethin. Then he headed to Butcherblock where he hunted in
        the valley on the way to the docks. Here lie not too many creatures, but
        the ones that do are scarabs and goblins, all my level or lower. I hunted
        those and went to 5th and made a ton of money. I hunted scattered large
        snakes at the opposite end of Butcherblock, and then went and checked
        out Crushbone. Someone had given me a Minotaur Axe at that point, so I
        could take on gambles by myself and hunted there for a while, and then
        headed back to Butcherblock to take out goblin camps through 6th level.
        I'm back in Crushbone for 7th, and after I hit 8th I'm headed to Steamfont
        to take on the creatures by the gate. After 9th I don't know what I'm
        doing, because I expect spells to change my life.
      <p>Inherently, Greater Faydark also has the benefit of having easy access
        within itself to good hunting sites. As you can tell from the above example,
        my wood elf spent a great deal of time travelling here, and the zone is
        excellent for anyone up to and including about 6th level. After that you
        really need to move on to Crushbone or one of the other zones on the island.
        I knew it was time to move on when I took out an orc centurion, an oracle,
        and a shaman all at the same time.
      <h3><font size=+2>T</font>ravelling <font size=+2>T</font>o and <font size=+2>F</font>rom
        <font size=+2>G</font>reater <font size=+2>F</font>aydark</h3>
      <p>To the North of Greater Faydark lies Crushbone, although there is no
        exit on the other side of Crushbone. Felwithe lies to the east of Crushbone
        in the southeastern corner of Greater Faydark. Approximately two-thirds
        of the way across the southern edge of the zone is the entrance to Lesser
        Faydark, which is how you get to Steamfont Mountains (take a left once
        you pass into Lesser Faydark at about 45 degrees and run for about 2 minutes,
        you'll hit the edge and the zone).
      <p>Following the path across the southern edge of Greater Faydark to the
        western wall you'll reach Butcherblock Mountains. You can reach this by
        following the path past Lesser Faydark to its completion.
      <p>Last, but not least, there is a set of portal towers located directly
        on the path between the zone edge to Lesser Faydark and the zone edge
        to Butcherblock. Follow the path east from them to head towards Felwithe,
        Kelethin, or Lesser Faydark, and west to Butcherblock Mountains.
