      <h3> <font size=+2>D</font>escription</h3>
      Nektulos Forest is a dark forest that lies north of the Commonlands on the
      eastern half of Antonica. It is home to a clan of halflings, and is also
      patrolled by the dark elves from nearby Neriak. It also represents the newbie
      zone for the dark elves from Neriak.
      <h3> <font size=+2>D</font>angers</h3>
      This area represents the newbie area for the dark elves, although the area
      is very small. The largest danger with the newbie area is that most of the
      monsters there will group with others of their kind, and because it is so
      small, one has to be careful to let the others get far enough away from
      their spawn spot so that they don't join in and send you running for the
      guards.
      <p>The other large problem is that the wandering monsters (pretty much only
        skeletons and wolves) are aggressive and will attack you. This can be
        fairly annoying as guards are only present at the bridge and the entrances
        to Neriak and Lavastorm. The Guardians will also attack good races, although
        they leave the evil races alone. On top of this, the orc runner occasionally
        makes a dash for the Neriak entrance. He is approximately 9th level, and
        his presence will make the normally benevolent dark elf guards attack
        ogres and trolls in the area, which is something to be careful of.
      <p>The last problem with this area, which isn't really a danger, is that
        this is, in my opinion, one of the worst designed areas in the game. I've
        started a character in every newbie area of the game and got to 5th level
        or so everywhere, and this area is by far the worst. The reasons are plenty,
        but basically the newbie area is very small, and wandering outside of
        it is very dangerous as 4th level wolves will attack and kill you. Therefore,
        if there are a large number of newbies there is very little to kill in
        this area. Also, once you reach 4th or 5th and start wandering a little
        bit further, Guardians are around you all the time. It's very frustrating
        to almost die in a battle with a skeleton, feel that you're going to win,
        and then have some stupid Guardian come up and kill it. Tada, no experience,
        no loot, 5 minutes of resting to recover. Another problem is that the
        creatures in this area don't give any significant loot. Unlike many other
        areas, it is difficult to find bags or boxes, rusty weapons, cash, bits
        of armor, etc on any of the monsters that you can hunt. Instead you get
        spiderling silks, ruined pelts, and fire beetle eyes. Needless to say,
        when I hit 4th level, I packed my bags and relocated to the swamps of
        the ogres and trolls. At least there I could get cash, maybe a cracked
        staff, and some netted armor for my enchanter...
      <h3> <font size=+2>B</font>enefits</h3>
      <p>As I mentioned above, I don't see much in the way of benefits in adventuring
        in this zone. There are a few places that are campable with a small group,
        most notably the halfling ruins (if you are evil) and the undead spawn
        spots, but other than those, random hunting is not very profitable in
        this area. </p>
      <p>The only exception to this is if you belong to one of the good races.
        You can make a good amount of experience and money by coming here and
        hunting the guards. Typically this is done at either of the zone exits
        and with people pulling the guards to there, which is safer because the
        zone is nearby. The guards are good experience and typically drop a bronze
        weapon every time.</p>
      <h3> <font size=+2>T</font>ravelling <font size=+2>T</font>o and <font size=+2>F</font>rom
        <font size=+2>N</font>ektulos <font size=+2>F</font>orest</h3>
      From the south there is a pass that leads into the Eastern Commonlands near
      Freeport. Through here dark elves can access the vast world beyond.
      <p>The entrance to Neriak lies in the northeastern corner on the east wall,
        and has a path leading nearby. The entrance to Lavastorm Mountains lies
        in the northwestern corner on the north wall.
