      <h3><font size=+2>D</font>escription</h3>
      <p>Ak'Anon is an ancient city, built into the edge of the mountains long 
        ago by the gnomes who call it home. Half fortress and half laboratory, 
        the gnomes have been developing their clockwork machines and defenses 
        here for generations, and show no signs of stopping. In ages past, the 
        gnomes were so powerful as to defy the gods and build their own clockwork 
        dragon to protect the city, but when it went rogue, it was either imprisoned 
        far beneath the city or banished to another land. Eons have passed since, 
        however, and the truth of the gnomes prior glory may never be known.
      <h3><font size=+2>S</font>urrounding <font size=+2>A</font>reas</h3>
      <p>The gnomes have long made their dwellings in the Steamfont Mountains 
        and, although they have not tamed that wild region, have established themselves 
        as a strong force there. They run several large windmills in the mountains, 
        generating power for some as-yet-unrevealed project, and the Observers 
        have set up their observatories in the far reaches of the mountains.
      <p>Further afield lies the vast untamed expanse of Lesser Faydark and, hidden 
        within, the lair of the evil vampire Mistmoore. The gnomes have a small 
        presence in this forest, more to protect their communications with the 
        elves and dwarves beyond than to stake a claim within the forest itself.
      <h3><font size=+2>T</font>ravelling <font size=+2>T</font>o and <font size=+2>F</font>rom 
        <font size=+2>A</font>k'<font size=+2>A</font>non</h3>
      <p>Ak'Anon can be reached by travelling through the Steamfont Mountains, 
        and lies nestled in a small valley in the southern edge of it. The ever-churning 
        machines of the gnomes can be seen marking the outside of their city, 
        and these may in fact be engines of war, although it is hard for those 
        who aren't gnomes (and some who are) to tell.
      <p>To the west lies the narrow chasm that opens into Lesser Faydark. The 
        small gnome outpost lies very near the opening on the other side, and 
        a short distance to the northwest lies the entrance to Greater Faydark 
        and the cities of the wood elves and high elves. Far to the west of Greater 
        Faydark lies the underground home of the dwarves.
      <h3><font size=+2>L</font>ocal <font size=+2>C</font>olor</h3>
      <p><b>Places</b> 
      <p>Ak'Anon reflects its inhabitants, unique and slightly reclusive, sticking 
        to their own while, at the same time, forming a cohesive whole. The separate 
        guild halls within the city lie far apart and in their own sections of 
        the city itself, but there are several central hubs of activity within 
        the city.
      <p>Prominent throughout the city is a large flowing river/lake that dominates 
        every area. This water is pumped from deep below the mountains by massive 
        machinery and allowed to flow throughout the city, providing power as 
        it does so and precious water to all the various parts and shops throughout 
        the city.
      <p>Far in the northern part of the city lies old caverns that have been 
        taken over by the less desirable elements of the gnome society, where 
        the necromancers and the other evil gnomes have taken up residence, as 
        well as the undead that they have summoned to protect their home.
      <p><b>People</b> 
      <p>The gnomes lie scattered throughout their city, inhabiting their small 
        sections of the city, typically distributed by their guild affiliation. 
        The spellcasters spend their time researching in the Library Mechanamagica, 
        the clerics worship in the Abbey of Deep Musing while the rogues hide 
        out beneath it. The evil gnomes hide far to the north in a forgotten part 
        of the city, where the water runs polluted by the city's machines, in 
        a some old mines there. The warriors live close by their king, in barracks 
        guarding the only land route in.
      <p>Scattered throughout the city lie guides, special clockworks designed 
        to provide information to those who are lost within the winding corridors. 
        Simply walking up to a guide and inquiring about the location of something 
        will lead to directions to reach it.



