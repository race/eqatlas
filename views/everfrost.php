      <h3><font size=+2>D</font>escription</h3>
      <p>Everfrost is a permanently frozen area of frequent snowfalls and constant
        snow cover. It is the northernmost zone, and is adjacent to the home of
        the barbarians, Halas. It is also adjacent to the gnoll home of Blackburrow,
        where all must pass if they want to enter the lands surrounding Qeynos,
        and Permafrost, a higher level dungeon to the east. Everfrost can be safely
        divided into two subzones, the canyons and the plains.
      <p>The canyons represent the newbie area for Halas. They are a twisting
        area of high ridges and very few open areas through which the lower level
        monsters move. This is also where the entrance to both Halas and Blackburrow
        lies.
      <p>The plains are a much vaster and more dangerous area. They lie to the
        east of the canyons and stretch for quite a ways further east before ending
        in the mountains. The only place further east is Permafrost. A frozen
        river divides this area into eastern and western halves, and ruins of
        an ancient civilization are scattered throughout the region.
      <h3><font size=+2>D</font>angers </h3>
      <p>The dangers can be divided into the two separate areas. The canyons can
        be dangerous for low level adventurers (less than 4th) as the 1st level
        monsters (the whelps and pups) are aggressive to you and you can wind
        up being swarmed by these creatures. It is also dangerous if you are in
        the business of attacking wolves. If I can give you advice, don't attack
        the wolves. Every zone on this half of the continent has wolves in it,
        and you don't want to constantly be harried by wolves, especially since
        there's no reason to kill them. Also, every monster in this area will
        join its comrades if they are in battle, so be careful of swarming. Also,
        there are now a trio of vengeful skeletons who appear identical to the
        normal weak ones. Be careful to /con all the skeletons you will be attacking
        or fighting near, or have an escape route planned out ahead of time.
      <p>The plains are an entirely different matter. The biggest problem here
        is that some of the most powerful creatures in the zone are also the most
        aggressive, namely the Orc Shaman and the Icebone Skeleton. The Orc Shaman
        can cast all manner of spells and can take on groups of level 11-13 characters,
        even though it is only about level 14-16. The Icebone Skeleton is difficult
        to see until it is upon you, and then does massive damage and stun attacks,
        as well as cast spells, and is about 15-16th level and immune to nonmagic
        attacks.
      <p>The other creatures on the plains that are dangerous are not aggressive,
        and these are the woolly mammoth. There are packs of people lining the
        western edge of the plains who send out a scout to bring in a mammoth
        or mammoth calf to kill here, and the only real danger is that they might
        bring back more than they can handle, and either perish or run to the
        guards, and bring the mammoths with them. The calves are about 14-16th
        level and the mamas are even higher (near 25th).
      <p>There is now a band of icy orcs that have taken up residence in the temple
        by the river and the ice giant statue. These creatures are approximately
        lvl 25, and have as their leader and Shadow Knight named Redwind, who
        is closer to 28th level or so. There is also a small network of caves
        off of the end of the northeast branch of the river. It is quite a swim
        from the nearest ice hole to the opening, with nowhere to breathe in between,
        so you need some method by which to breathe water. Within the caves can
        be found a aggressive deadly necromancer, several Icebone Skeletons, and
        the elusive cave bear.
      <h3><font size=+2>B</font>enefits</h3>
      <p>The canyons of Everfrost are probably one of the most wealthy hunting
        grounds I've ever found. I consistently would return from hunting there
        with 2-3 platinum, even at low levels (like 3rd). By then you're killing
        all the creatures that walk by, sometimes in hoards, and raking in the
        money and experience. The bears have pelts, the skeletons have weapons,
        and the pups and whelps quite often carry sacks.
      <p>The plains are good for wealth if you are of significantly high level
        such that you can hunt in a group and take out mammoths. Their tusks are
        worth quite a bit, and it is quite safe to hunt them the way they do,
        in a very controlled environment and with guards nearby. Also, a midlevel
        (5th or 6th) soloist could make quite a living taking out Ice Goblins
        along the edge, including their little camps. The other benefit to hunting
        in the deep plains is that there are not a lot of people around, so the
        monster you spot is more than likely yours. There are also some unique
        quest items floating around.
      <h3><font size=+2>T</font>ravelling <font size=+2>T</font>o and <font size=+2>F</font>rom
        <font size=+2>E</font>verfrost</h3>
      <p>To the southeast of the canyons lies the entrance to Blackburrow from
        Everfrost. It can be difficult to find, but is generally safe. Be careful,
        however, because the Everfrost section of the tunnels has both gnolls
        and darkness, you need to be able to take them out and see or you'll never
        reach the other end.
      <p>To the East lies the entrance to Permafrost. I have never gone inside,
        I usually get creamed by an Icebone Skeleton before I reach there and
        I can track the ice giants within, but it exists on the far northern edge
        of the eastern edge of the plains.
