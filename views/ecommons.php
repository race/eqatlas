      <h3><font size=+2>D</font>escription</h3>
      <p>The East Commonlands is a large open area that is adjacent to West Freeport,
        and contains both an area for newbies to train in as well as for adventurers
        that are more experienced. It is something of a melting pot, like Northern
        Desert of Ro, because many races mix in the area.
      <h3><font size=+2>D</font>angers </h3>
      <p>The main danger in this zone is the Griffins that fly around and terrorize
        everyone. They are the highest level creature in the zone (beneath them
        are probably the rare mummy, and most of the others are less than tenth
        level), and fly from one end to the other, attacking all characters they
        encounter. Their danger is enhanced by the fact that they frequently can
        be seen floating in mid-air, when in fact they are much further away,
        or closer, because they haven't popped yet. It is good to know the numbering
        system for the Inns (numbers one through four, starting at the one closest
        to Freeport) as people will usually shout the location of the griffin
        as it makes it sweeps through the area.
      <p>Some of the other creatures are aggressive, most notably the undead and
        the orcs, but are readily avoidable if one is paying attention to where
        things are. Hunting the orcs in their camps is also dangerous, as oracles
        will frequently spawn in them. There are also typically large groups of
        players camping them.
      <h3><font size=+2>B</font>enefits</h3>
      <p>I'll be honest when I say that I feel East Commonlands is solely a place
        to pass through when travelling to better hunting. The number of random
        deadly creatures in this area make it a dangerous area for newbies, and
        for higher level characters there is simply not enough here to hunt on
        a regular basis. The exception to this is when snake hunting along the
        southern desert edge, or in one of the groups camping for the orcs.
      <p>There is also some fair hunting for solo and small groups by wandering
        throughout the zone and picking on giant scarabs, spiders, and young kodiaks,
        although you have to be around 10th level to do this.
      <p>As far as being a waypoint, this area is good because it provides a sort
        of waystation for most groups so that they don't have to travel far to
        sell goods or buy food. The area supplies characters with arrows, tiny
        daggers, food and several types of armor without leaving the area. It
        is also a convenient place to purchase trade items, such as gems and cooking
        and pottery supplies. For dark elves, this area is a boon, as most of
        the guards leave dark elves alone. This is also where the underground
        market is, notable for the Shady Swashbuckler who hangs out in the middle.
        Many of the player run bazaars and gatherings are held here, since it
        is free of monsters and safe for all races. It lies in the tunnel to North
        Ro.
      <h3><font size=+2>T</font>ravelling <font size=+2>T</font>o and <font size=+2>F</font>rom
        <font size=+2>E</font>ast <font size=+2>C</font>ommonlands</h3>
      <p>East Commonlands is readily accessible, and provides a major hub for
        travel throughout Antonica. To the east lies West Freeport. To the west
        lie the West Commonlands, a very similar area to this one.
      <p>North lies a gap in the hills through to Nektulos Forest, the dark forest
        of the dark elves. To the south lies the tunnel that leads to the Northern
        Desert of Ro, where the underground market is.
