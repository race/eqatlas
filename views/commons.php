      <h3><font size=+2>D</font>escription</h3>
      <p>The West Commonlands is a large open area that is the western continuation
        of East Commonlands. This area is significantly higher level than East
        Commonlands, and many of the monsters here are both dangerous and aggressive.
      <h3><font size=+2>D</font>angers </h3>
      <p>This is an area, much like Northern and Eastern Karana, where many relatively
        low-level players travel through in order to go from Qeynos to Freeport.
        It also has a large number of aggressive creatures that live in it, most
        notably the hill giants and kodiaks, but the undead and werewolves also
        fit the bill.
      <p>Aside from the above, low-level characters (below 10th) shouldn't really
        be in this zone at all, and travellers should stick to the path and keep
        an eye out for wandering aggressive creatures. The griffins and hill giants
        will wander all over the zone, and are (surprisingly) difficult to spot
        sometimes. Shadowed Men are also here, and are quite powerful, but they
        typically stay to their camp in the southwest corner of the zone.
      <h3><font size=+2>B</font>enefits</h3>
      <p>This area is a good, solid, higher-level hunting area. There are several
        camps that can be taken by groups of lower level characters, and most
        of the rest of the zone can provide good hunting for higher levels. It
        is also a great area to hunt Will O' Wisps in, and they start at 9th level.
      <p>There are also several quests that involve this region, and it is where
        the entrance to Befallen exists, quietly tucked away behind where the
        hill giants roam in the southern edge of the zone.
      <h3><font size=+2>T</font>ravelling <font size=+2>T</font>o and <font size=+2>F</font>rom
