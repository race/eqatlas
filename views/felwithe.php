      <h3><font size=+2>D</font>escription</h3>
      <p>Felwithe is the legendary city of the high elves, which rises out of
        the fog of Greater Faydark as one of the most wonderful cities in all
        of Norrath. Its golden walls give off a faint glow all their own, and
        the peaceful city attracts many visitors from abroad.
      <h3><font size=+2>S</font>urrounding <font size=+2>A</font>reas</h3>
      <p>Greater Faydark is the forest that Felwithe lies within. From there,
        high elves may access Butcherblock Mountains and the lands beyond, Lesser
        Faydark, or the orc lair of Crushbone.
      <h3><font size=+2>T</font>ravelling <font size=+2>T</font>o and <font size=+2>F</font>rom
        <font size=+2>F</font>elwithe</h3>
      <p>Felwithe's entrance lies due east of the pass from the Butcherblock Mountains,
        along a path that winds its way around the periphery of Greater Faydark.
        Lesser Faydark is through a pass halfway between Butcherblock and Felwithe,
        and there are a set of transportation towers along that path.
      <p>To the northwest of Felwithe lies the wood elf home of Kelethin, and
        north of that lies the lair of the Crushbone Orcs.
      <h3><font size=+2>L</font>ocal <font size=+2>C</font>olor</h3>
      <p><b>Places</b>
      <p>The town is dominated by guild halls to the various followings. One of
        the wonders of architecture lies in the far end of Felwithe, the tower
        of the mages, suspended unbelievably over a pool below. There is also
        an extensive river running through the city, and fishing is a very common
        pastime here.
      <p><b>People</b>
      <p>The high elves are a reserved, contemplative people, much like the Erudites
        in that regard. As such, there are no open markets or the associated "undesirables"
        within its walls. Quiet thought is the norm in Felwithe, and its inhabitants
        reflect that.
