      <h3> <font size=+2>D</font>escription</h3>
      The Northern Desert of Ro is a highly trafficked and popular area that marks
      the southern edge of the human city of Freeport as well as the northern
      edge of the vast Desert of Ro. It also represents a major waypoint for the
      dark races that pass from Neriak to the swamps to the south, and many of
      their kind can be found here.
      <h3> <font size=+2>D</font>angers</h3>
      This zone generally provides some good, safe hunting, mostly in the northern
      part of the desert, where adventurers can find non-hostile creatures that
      don't group together to battle. The dangerous part of this zone comes about
      because there are several types of higher-level roaming single creatures
      that are very aggressive and dangerous. The most common are the madmen,
      who are extremely aggressive but also easily distracted and slow runners.
      The rarer ones are the various mummies, who are also slow but can cause
      disease with their attacks. And, of course, there are the most dangerous
      creatures, the sand giants, who generally stay to the southern edge of the
      zone but travel about half way up in their walking.
      <p>Of the dangers that are more predictable, though, the most dangerous
        is the Dervish camps. These are generally camped out by adventurers hunting
        for good experience and Dervish rings to sell elsewhere. Although the
        Dervishes generally spawn at a regular rate, sometimes several of them
        spawn at once and can be of quite varying levels, and can occasionally
        include a Thug, which are very powerful in melee combat.
      <p>Also of note is Dorn B'Dynn. This is a truly evil Dark Elf that spawns
        in the northernmost Dervish camp and is the bane of all the travellers
        of this desert. The problem is that he is much more powerful than he appears,
        which leads many adventurers to attempt to take him out, only to fail
        miserably and have to run to the zone. Once out, Dorn can't be stopped,
        as guards won't attack him (they're apparently not as stupid as they look)
        and he runs around until someone kills him. Just for reference, my druid
        (who, of note, has been able to solo even rare NPC spellcasters on several
        occasions) has serious trouble soloing Dorn when he is so green that he
        doesn't even give experience any more.
      <h3> <font size=+2>B</font>enefits</h3>
      The Northern Desert of Ro is a very popular area for a number of reasons.
      First of all, it is very much like a marketplace for higher level characters,
      as many of the higher level characters in the Freeport area hunt somewhere
      in the Desert of Ro and travel back and forth to Freeport through this zone.
      If you are looking for an item or items, this is a good place to start,
      as many merchants come here to sell their wares, or know of someone else
      who does.
      <p>The other bonus to this area is that there is a great deal of good hunting
        here. Yes, there are a lot of people camping out the major sites where
        monsters appear (the Dervish camps, the undead sites), but there is still
        a good deal of hunting for groups levels 4-10 and solo levels 7-15.
      <h3> <font size=+2>T</font>ravelling <font size=+2>T</font>o and <font size=+2>F</font>rom
        <font size=+2>N</font>orthern <font size=+2>D</font>esert of <font size=+2>R</font>o</h3>
      The Northern Desert of Ro can be reached from the south by a vast border
      with the Oasis of Marr. There is no narrow pass to go through, just walk
      over the dunes.
      <p>The northern edge of the desert has a wide border with East Freeport
        and the newbie area there. Also, on the northwestern corner, tucked away
        behind the large stone spire, lies the infamous "Tunnel" to the East Commonlands.
        The route is long, but generally safe and is heavily travelled as people
        who would rather bypass Freeport altogether travel through here.
      <p>There has also been a new dock opened up by the gnomes of Faydwer for
        people wishing to travel to the continent of Velious.
