      <h3><font size=+2>D</font>escription</h3>
      <p>Kaladim is the underground city of the Dwarves of Norrath. A huge carved
        statue of a dwarf marks the entrance of their city onto the rugged Butcherblock
        Mountains.
      <h3><font size=+2>S</font>urrounding <font size=+2>A</font>reas</h3>
      <p>Butcherblock Mountains is a vast expanse of rough terrain on the north
        end of which lies Kaladim. The dwarves have guard stations scattered throughout
        the mountains, but they have been unable to claim the region for their
        own, and many dangerous monsters still wander around freely.
      <h3><font size=+2>T</font>ravelling <font size=+2>T</font>o and <font size=+2>F</font>rom
        <font size=+2>K</font>aladim</h3>
      <p>Kaladim's entrance is along the northern border of the Butcherblock Mountains.
        To the west of this entrance lies the port where the dock from the continent
        of Antonica arrives, taking travellers to and from Freeport. Far in the
        other direction, through the mountain passes, lies the entrance to the
        forest of the elves, Greater Faydark. To the south lies the narrow pass
        to Dagnor's Cauldron.
      <h3><font size=+2>L</font>ocal <font size=+2>C</font>olor</h3>
      <p><b>Places</b>
      <p>This town is completely carved out of the mountains, and winds its way
        ever deeper. The guilds have each carved out their own niches in different
        areas of the caverns, forming a network of warrens that all dwarves call
        home.
      <p><b>People</b>
      <p>The dwarves wander throughout their town, busy on business and protecting
        their community from the pressures outside. Many of the strongest warriors
        of the land, as well as its most devout, hail from these halls, and the
        inhabitants carry themselves with pride because of it.
