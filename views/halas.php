      <h3><font size=+2>D</font>escription</h3>
      <p>Halas is a city like its inhabitants, simple and to the point. Only barbarians
        would even consider swimming in the lake that provides them protection
        instead of waiting for the raft to carry them across, swimming without
        fear in the frigid waters. The city is relatively small, but has everything
        that any larger city has in half the space. Barbarians are usually the
        only ones in the streets, but you can occasionally see a human or half-elf,
        or even other more tropical races. Frequent snowstorms keep the streets
        and rooftops white year-round.
      <h3><font size=+2>S</font>urrounding <font size=+2>A</font>reas</h3>
      <p>The area surrounding Halas is an area deceptively teeming with life.
        Many creatures have adapted to living in the cold, and regularly roam
        the Everfrost Peaks. The narrow valleys of these mountains make some of
        the stronger creatures even more dangerous, as there is often nowhere
        to flee from them if need be. Perhaps this is why the barbarians have
        developed into one of the bravest races in Norrath, standing the ground
        while a lesser man might flee.
      <p>From the Everfrost Peaks, an intrepid explorer can find the cavern home
        of the Blackburrow gnolls, a vicious enemy of the Barbarians. Through
        their home lies the way to other lands to the south of the Barbarians'
        homeland. Many adventurers, often from far off lands, can be found braving
        the wastes of the Permafrost, also adjacent to the Everfrost Peaks. Rumors
        of ancient civilizations and ruins dotting the snowy wastes bring in many
        a curious explorer (such as myself).
      <h3><font size=+2>T</font>ravelling <font size=+2>T</font>o and <font size=+2>F</font>rom
        <font size=+2>H</font>alas</h3>
      <p>The only way to get to the city of Halas is to brave both the gnoll land
        of Blackburrow as well as the Everfrost Peaks themselves. Passing through
        these leads to the lands north of Qeynos and, from there, the rest of
        Norrath.
      <h3><font size=+2>L</font>ocal <font size=+2>C</font>olor</h3>
      <p><b>Places</b>
      <p>The city of Halas has more than its share of bars, probably to keep these
        tundra dwellers warm on a winter night. These are humming with life during
        the drinking festivals, carried out regularly across Norrath on the same
        evenings every week. Armories are also a common sight, and frequented
        almost as much as the bars. Most of the inhabitants of this city know
        well how to use a sword, and I almost feel badly for the gnolls to whom
        they are mortal enemies.
      <p><b>People</b>
      <p>The people of Halas generally keep to themselves. Their life requires
        a great deal of what other races might term menial work, so their days
        are consumed with the process of survival. This is not to say that there
        aren't very frequent brawls in the streets as young (and old) warriors
        demonstrate their skills for others, or drinking bouts ending in dozens
        of unconscious champions, but the Barbarians could be termed a simple
        people.
