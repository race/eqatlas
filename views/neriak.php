      <h3><font size=+2>D</font>escription</h3>
      <p>Neriak is the center of the Dark Elven empire, and is a splendid, dark
        city that lies carved out of the mountians next to the Nektulos Forest.
        No natural light has ever seen their homeland, and the dark elves thrive
        and flourish in their city. All manner of evil races, including the Trolls
        and Ogres, can be found within. Most of these are restricted to the Foreign
        Quarter, where the dark elves trade with the outside world. The deeper
        parts of the city house many of the city's shops, guild halls, and homes.
      <h3><font size=+2>S</font>urrounding <font size=+2>A</font>reas</h3>
      <p>Neriak lies off of the Nektulos Forest, a barely alive forest that is
        continuously in the dark from the smoke drifting down from the volcanic
        Lavastorm Mountains to the north. To the south of the forest lies the
        Commonlands and the homes of their enemies, the humans.
      <h3><font size=+2>T</font>ravelling <font size=+2>T</font>o and <font size=+2>F</font>rom
        <font size=+2>N</font>eriak</h3>
      <p>Neriak can be reached only by travelling through the Nektulos Forest
        to its well-guarded entrance. To the north of the forest lies the broken
        Lavastorm Mountains and the lair of the great dragon, Lord Nagafen, as
        well as the Temple of Solusek Ro. To the south of the forest lies the
        Commonlands, and from them the human cities of Freeport, Highhold Keep,
        and the lands beyond.
      <h3><font size=+2>L</font>ocal <font size=+2>C</font>olor</h3>
      <p><b>Places</b>
      <p>Neriak is a twisted, but beautiful, city, carved out of the earth millenia
        ago. Magic and excellent artisanship have lit up the city with glowing
        neon-colored lights, and fountains and monuments to the god of the dark
        elves, Innoruuk, fill the city.
      <p><b>People</b>
      <p>The city of Neriak, for all of its beauty, is filled with danger, as
        befits its occupants. Stepping into the wrong place in any part of the
        city can easily get you killed, and this is especially true for the Trolls
        and Ogres, who, although guests, are not welcome everywhere. This could
        be from some degree of superiority on the dark elves part, and also because
        of the plot the dark elves are hatching, lying barely hidden beneath the
        surface of the city.
