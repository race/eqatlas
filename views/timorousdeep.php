      <h3><font size=+2>D</font>escription</h3>
      <p>The Timorous Deep is the name of the ocean that lies between Kunark and
        the other continents of Norrath. The Deep is dotted with islands that
        stretch across it, and these islands contain a mixture of the modern powers
        on Norrath, those that have fallen out of their societies, natural predators,
        and ancient civilizations.
      <h3><font size=+2>D</font>angers </h3>
      <p>The Deep is separated into many separate islands, some of which are much
        more dangerous than others. Probably the most dangerous island is the
        Raptor islands. The raptors that live on these are very powerful (mid-40's
        in level) and very aggressive, with huge aggro ranges, and it seems that
        if you can even see one it will attack you once you step foot on the island.
        Apparently, their clawed feet are also webbed, and they can swim faster
        than you as well.
      <p>The other aggressive creatures are the powerful golras on the iceberg.
        These are around mid-20th in level, but occasionally a much more powerful
        golra named Halara appears here. All the golras are aggressive to everybody.
        There is also a powerful aggressive ogre that appears in the ogre encampment
        named the Great Oowomp, who can wreak havoc with any group hunting ogres
        and unlucky enough to pull him as well.
      <p>There is also a dragon that can be found in the southwestern corner of
        the Deep around the Combine Teleportation Room. This dragon is aggressive
        and found in the water, and as powerful as Nagafen. This makes him very
        difficult to kill and almost impossible to avoid. He is rarely around,
        however, as he is a triggered spawn and is quickly killed by an NPC in
        the zone if the group that triggered him can't.
      <p>The other creatures in the zone are also aggressive, like the bandits,
        the undead, and the ogres and elves to those with bad faction with them,
        and all these creatures will group together. The Spirocs will also group
        together and fight, and are as powerful as the raptors, although less
        aggressive.
      <p>Timorous Deep is also a place that one can easily get lost in. There
        are vast stretches of nothing but water, and either knowing how to use
        the /loc command or sensing headings can make up a lot of potentially
        lost time.
      <h3><font size=+2>B</font>enefits</h3>
      <p>Timorous Deep has very good hunting for a wide range of people, and most
        of the places to hunt are relatively untouched by hordes of other players.
        This means that at most times it's possible to find a place to hunt in
        the Deep where you won't be fighting someone for this. The exception to
        this is probably the ogre camp, which is pretty busy of late.
      <p>The Deep is also one of the areas in the game where many people have
        never seen. There are some nice sites to see in the zone, and it is very
        fun to explore. One of the nicest sites is the Inn hidden away in the
        southermost edge of the Deep, likely the old hideaway for some hermit.
      <p>There is, way in the southwestern corner of the zone, an underwater portal
        room that lies through a small tunnel on the ocean bed in the midst of
        a Combine Teleportation Ring. There are fire pots in this room that will
        teleport you one way to many of the cities of Norrath (excepting most
        human cities).
