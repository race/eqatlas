      <h3><font size=+2>D</font>escription</h3>
      <p>The Lavastorm Mountains are a relatively small, but very difficult and
        dangerous zone to travel through. It is similar to Dagnor's Cauldron,
        in that some cataclysm has wrecked the terrain within it, here being obviously
        volcanic in nature. There are many lava pits throughout the zone, and
        shattered rocks stick up from the ground. A massive lava pit, known as
        Solusek's Eye, lies in the center along the northern edge of this shattered
        land.
      <h3><font size=+2>D</font>angers </h3>
      <p>Lavastorm is a good stepping stone for hunting through the teens, as
        it is filled with monsters at those levels.. There is no monster here
        than can be attacked by someone of less than 12th level or so (although
        some people with pets can do so), while adventurers 16th-18th will be
        able to hunt well, and higher level adventurers will be hard pressed to
        find anything to occupy themselves. The main dangers are the creatures
        within the zone. Of all the creatures above, only a handful are aggressive,
        and that includes the goblins, the fire elementals, the fire imp, and
        the shadowed men. Of these, the ones that are extraordinarily dangerous
        are the fire imp and the shadowed men. The fire imp is one of those monsters
        that /con's much lower than they actually are. Fighting a green aggressive
        one at level 17 as a ranger with both fire and magic protection over 50%,
        I was killed rather easily because this creature casts a rather powerful
        fire-based spell throughout battle. The shadowed men are a special case,
        as they come in a rather large group and don't wander, and are well-known
        to be both fast on the ground and lethal in combat, regularly tearing
        apart larger groups of more powerful characters. They are also approximately
        30th level, which makes them the most powerful creatures in the zone to
        date.
      <p>The other danger is the terrain itself. The harsh rock outcroppings and
        ravines can hide drops that will cause you damage (especially if taken
        quickly), pits of lava, or aggressive monsters. Travelling slowly and
        with care, or quickly with levitate, are the safest ways to get through
        the zone.
      <h3><font size=+2>B</font>enefits</h3>
      <p>As a ranger who prefers to hunt outdoors, I enjoy hunting in this zone
        to most of the dungeons around. The monsters are relatively scattered
        so you can control exactly what you fight. All of the creatures spawn
        items that are worth a fair amount of money. They are all also extremely
        cool creatures, nowhere else can you hear the sounds of the rock dervish
        as its many rocks slam into your armor in rapid unison, or hunt the manner
        of creatures that are seen here.
      <p>There are some more practical benefits to hunting in the zone as well.
        The good races can sell at the gypsies located along the west wall, and
        the evil races (dark elves at least) can sell to the druid merchant at
        the druid ring in the northwest corner (don't ask me why that makes sense).
        Also, there are many higher level druid spells for sale there. There are
        *four* dungeons accessible from this zone, making it a crossroads of higher
        level characters to and from hunting grounds within the various dungeons.
      <h3><font size=+2>T</font>ravelling <font size=+2>T</font>o and <font size=+2>F</font>rom
        <font size=+2>L</font>avastorm</h3>
      <p>Lavastorm can be reached from the south by trekking through the Nektulos
        Forest, home to the dark elves. Exits to Najena lie in the southeast corner
        of the zone, behind a massive set of shattered doors. Entrances to Solusek's
        Eye A and B lie along the southern edge of Solusek's Eye, and the Temple
        of Solusek Ro lies along its northern edge.
